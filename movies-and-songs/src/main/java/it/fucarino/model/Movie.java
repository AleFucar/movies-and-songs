package it.fucarino.model;



public class Movie {
	
	private long id;
	private String nameString;
		
	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}


	public String getNameString() {
		return nameString;
	}

	public void setNameString(String nameString) {
		this.nameString = nameString;
	}
	
	
	
}
